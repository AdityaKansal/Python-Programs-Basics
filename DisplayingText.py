# 1 Simple text
print('1 - ' + 'Aditya')
print('Kansal')
#2 Line break using \n
print('\n')
print('2 - ' + 'Aditya\nKansal')


#3 line break using triple quotes """  :- In this whatever we type, it will go in same format
print('\n')
print('3 - ' + """Hello
Aditya
Kansal""")


#4 concatenating two strings
print('\n')
print('4 - ' + 'Aditya' + 'Kansal')


#5 Inserting Tab
print('\n')
print('5 - ' + 'Aditya\tKansal')


#6 Typing using double quotes ""
print('\n')
print("6 - " +"Aditya Kansal")


#7 Priting single quote and double quote with a trick
print('\n')
print('7 - ''"Aditya"'+ " 'Kansal'")


# 8 Printing \n with a trick
print('\n')
print('8 - ' + ' \\n')


